package by.instruction.papera;

public class Topics {
    String topicName;
    String fileName;

    public Topics(String topicName, String fileName) {
        this.topicName = topicName;
        this.fileName = fileName;
    }

    public String getTopicName() {
        return topicName;
    }

    public String getFileName() {
        return fileName;
    }
}

